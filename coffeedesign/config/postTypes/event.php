// Set default taxonomy to "Friday Morning"
function setDefaultTaxonomy( $post_id ) {
  $current_post = get_post( $post_id );
	$taxonomies   = get_object_taxonomies( $current_post->post_type);
	foreach ( (array) $taxonomies as $taxonomy ) {
		$terms = wp_get_post_terms( $post_id, $taxonomy );
		if ( empty( $terms ) ) {
			wp_set_object_terms( $post_id, 'friday-morning', 'eventType', true );
		}
	}
}

add_action( 'save_post_event', 'setDefaultTaxonomy' );
